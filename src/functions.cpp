#include "functions.h"

int Menu::sum(int a, int b)
{
    return a + b;
}

int Menu::subtraction(int a, int b)
{
    return a - b;
}

int Menu::multiplication(int a, int b)
{
    return a * b;
}

int Menu::division(int a, int b)
{
    return a / b;
}
int Menu::powerOfNumber(int a, int b)
{
    return pow(a, b);
}